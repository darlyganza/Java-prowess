package Basics;

public class DataTypes {
	public static void main(String[] args) {
		//java data types are primitive and complex ones:
		//primitive: 
		int age = 12;
		System.out.println(age);
		
		//showcasing casting: 
		
		long number = 123456789;
		int num1 = (int)number;
		short num2 = (short)number;
		System.out.println(num2);
		
		
		
	}

}
